// Stats counter animation
const statsObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const statNumber = entry.target.querySelector('.stat-number');
            const targetValue = statNumber.textContent;
            
            // Check if it's a number or includes special characters
            if (targetValue.includes('+')) {
                animateValue(statNumber, 0, parseInt(targetValue), 2000, '+');
            } else if (targetValue.includes('%')) {
                animateValue(statNumber, 0, parseInt(targetValue), 2000, '%');
            } else if (targetValue.includes('.')) {
                animateFloatValue(statNumber, 0, parseFloat(targetValue), 2000);
            }
            
            statsObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

// Animate integer values
function animateValue(element, start, end, duration, suffix = '') {
    const range = end - start;
    const increment = range / (duration / 16);
    let current = start;
    
    const timer = setInterval(() => {
        current += increment;
        if (current >= end) {
            element.textContent = end + suffix;
            clearInterval(timer);
        } else {
            element.textContent = Math.floor(current) + suffix;
        }
    }, 16);
}

// Animate float values
function animateFloatValue(element, start, end, duration) {
    const range = end - start;
    const increment = range / (duration / 16);
    let current = start;
    
    const timer = setInterval(() => {
        current += increment;
        if (current >= end) {
            element.textContent = end.toFixed(2);
            clearInterval(timer);
        } else {
            element.textContent = current.toFixed(2);
        }
    }, 16);
}

